// Assignment operations

import static java.lang.System.*;

public class Assignment
{
   public static void main( String args[] )
   {
		int num = 10;
		out.println(num);

		num = num + 5;
		out.println(num);

		num = 10 * 2 + 7;
		out.println(num);

		num *= 2;
		out.println(num);

		num /= 5;
		out.println(num);

		num = num + 4 / 2 - 8;
		out.println(num);

		num = (4 + 5)/2+7;
		out.println(num);
   }
}

