// integer casting

import static java.lang.System.*;

public class IntCast
{
   public static void main( String args[] )
   {
		out.println("1/2 = " + (1/2));
		out.println("(double)1/2 = " + (double)1/2);
		out.println("5/2 = " + (5/2));
		out.println("5/(double)2 = " + 5/(double)2);
   }
}
