//modulus % example

import static java.lang.System.*;

public class Modulus
{
   public static void main( String args[] )
   {
		out.println( "% gives you the remainder of division!");

		out.println("2 % 3 = " + 2 % 3);
		out.println("3 % 2 = " + 3 % 2);
		out.println("45 % 10 = " + 45 % 10);
		out.println("10 % 45 = " + 10 % 45);
		out.println("9 % 3 = " + 9 % 3);
		out.println("9.2 % 3 = " + 9.2 % 3);
		out.println("8 % 3.2 = " + 8 % 3.2);
		out.println("9.2 % 3.1 = " + 9.2 % 3.1);
	}
}
