//division example

import static java.lang.System.*;

public class Divide
{
   public static void main( String args[] )
   {
		out.println( "Integer division   1 / 2 == " + (1 / 2) );
		out.println( "Integer / double division  1 / 2.0 == " + (1 / 2.0) + "\n\n");

		out.println(2 / 3);

		out.println(3 / 2);

		out.println(9 / 3);

		out.println(9.2 / 3);
   }
}
