// shortcut operators

import static java.lang.System.*;

public class ShortCuts
{
   public static void main( String args[] )
   {
		int num = 11;
		out.println(num);

		num++;
		out.println(num);

		num++;
		out.println(num);

		num--;
		out.println(num);

		num++;
		out.println(num);

		num*=5;
		out.println(num);

		num*=2;
		out.println(num);
		
		num*=2;
		out.println(num);	
		
		num/=2;
		out.println(num);			
   }
}

